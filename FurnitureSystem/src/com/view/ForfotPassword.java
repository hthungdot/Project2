    private void btnContinueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinueActionPerformed
        if (!check().equals("")) {
            Common.notify(check(), notification, 2);
        } else if (!Dao_ForgotPass.checkUser(txtUsername.getText(), txtEmail.getText())) {
            Common.notify("Incorrect account or email!", notification, 2);
        } else {
            Dao_ForgotPass.excuteForgotPassword(txtUsername.getText(), txtEmail.getText());
            Common.notify("A new password has been sent to your email." + "\n" + "Please check email!!", notification, 1);
            Login jlogin = new Login();
            Login.fp.dispose();
            Login.jlogin.setLocationRelativeTo(null);
            Login.jlogin.setVisible(true);
        }
    }//GEN-LAST:event_btnContinueActionPerformed

