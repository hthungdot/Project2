    private String check() {
        String message = "";
        if (!Common.checkNotEmpty(txtUsername)) {
            message += "You have not entered username!" + "\n";
            lblUsername.setForeground(Color.red);
        }
        if (!Common.checkNotEmpty(txtEmail)) {
            message += "You have not entered email!" + "\n";
            lblEmail.setForeground(Color.red);
        } else {
            lblEmail.setForeground(Color.white);
            if (!Common.checkFormat("^[A-Za-z]\\w*(.\\w)*@\\w+(.\\w{2,3})+$", txtEmail)) {
                message += "Email address is not correct!" + "\n";
                lblEmail.setForeground(Color.red);
            }
        }
        if (message.equals("")) {
            lblUsername.setForeground(Color.white);
            lblEmail.setForeground(Color.white);
        }
        return message;
    }

