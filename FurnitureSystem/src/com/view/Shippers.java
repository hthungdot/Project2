    public void chek() {
        String phone = jTextField6.getText();
        String co = com.getText();
        boolean bl = Pattern.matches("[0-9]{10,13}", phone);
        if (phone.equals("") || co.equals("")) {
            JOptionPane.showMessageDialog(null, checkFullInfo);
        } else if (bl == false) {
            JOptionPane.showMessageDialog(null, checkFormatPhone);
        } else if (bl == true && phone.length() > 9 && phone.length() < 14) {
            checkHT();
        }
    }
