    public void CheckChange() {
        String mkm = jTextField5.getText();
        String confirm = jTextField6.getText();
        if (mkm.equals("") || confirm.equals("") || jTextField1.equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter full information");

        } else if (mkm.equals(confirm)) {
            changePass();
            reset();
            JOptionPane.showMessageDialog(null, "Change Password Successfully");
        } else {
            JOptionPane.showMessageDialog(null, "Confirm Password Incorrect");
        }

    }
